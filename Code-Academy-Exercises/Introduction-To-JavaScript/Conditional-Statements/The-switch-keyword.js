let athleteFinalPosition = 'first place';
switch (athleteFinalPosition) {
    case 'first place':
    console.log('You get the gold medal!');
    break;
  case 'second place':
    console.log('You get the silver medal!');
    break;
  case 'third place':
    console.log('You get the bronze medal!');
    break;
    default:
    console.log('No medal awarded.');
    break;
}

// i learned about the switch keyboard in JavaScript, which lets you check for a value without having to write an else if statement every time. A switch statement provides an alternative syntax that is easier to read and write. 
